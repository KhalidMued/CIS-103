package AAS_One;

public class Multiplication {

	public static void main(String[] args) {

		
		
			//Outer Loop
			 for (int y = 1; y < 12; ++y) {
				 
			    //Inner Loop
			     for (int x = 1; x < 12; ++x) {
			    	  
			        System.out.printf("%11d", y*x);
			      }
			      
			      System.out.println();
			    }
			  
			
	}

}
